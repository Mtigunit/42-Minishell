#include "t_parser.h"

int	t_parser_argend(char c)
{
	return ((c == '>')
		|| (c == '<')
		|| (c == '|')
		|| (c == '(')
		|| (c == ')')
		|| (c == '&')
		|| (c == '\t')
		|| (c == '\n')
		|| (c == '\v')
		|| (c == '\f')
		|| (c == '\r')
		|| (c == ' '));
}
