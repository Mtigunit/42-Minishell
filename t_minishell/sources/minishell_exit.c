#include "minishell.h"
#include "t_terminal.h"
#include "minilibc.h"
#include <unistd.h>

void	minishell_exit(void)
{
	t_terminal_fini();
	write(2, "exit\n", 5);
	ft_exit(ft_getstatus());
}
